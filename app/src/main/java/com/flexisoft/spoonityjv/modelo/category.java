package com.flexisoft.spoonityjv.modelo;

public class category {
    String vendor_catalog_id;
    String identifier;
    String name;

    public String getVendor_catalog_id() {
        return vendor_catalog_id;
    }

    public void setVendor_catalog_id(String vendor_catalog_id) {
        this.vendor_catalog_id = vendor_catalog_id;
    }

    public String getIdentifier() {
        return identifier;
    }

    public void setIdentifier(String identifier) {
        this.identifier = identifier;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
